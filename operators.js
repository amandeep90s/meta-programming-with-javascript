/**
  Arithmetic operators
  + addition
  - subtraction
  / division
  * multiplication
 */

/**
  Logical operators
  && and
  || or
  ! not
 */

console.log(2 + 2);
console.log(3 - 1);
console.log(4 / 2);
console.log(3 * 2);
console.log(5 > 10 && 10 > 5);
console.log(5 > 10 || 10 > 5);
console.log(4 == 4);
console.log(!(5 != 6));
